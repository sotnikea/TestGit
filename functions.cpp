#include "functions.h"

// Substract two values
int SubstractFunc(int num1, int num2)
{
	return num1 - num2;
}

// Sum two values
int SumFunc(int num1, int num2)
{
	return num1 + num2;
}

// Multiply two values
int MultFunc(int num1, int num2)
{
	return num1 * num2;
}

// Divide two values
int DivideFunc(int num1, int num2)
{
	return num1 / num2;
}